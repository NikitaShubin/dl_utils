#!/bin/sh

# Перезапускает команду всякий раз, когда она завершается ошибкой.

while true; do
    "$@" && break
    sleep 1
    
    # Отмечаем момент перезапуска:
    echo
    echo '┍━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┑'
    echo '│                                     │'
    date +"│  Перезапуск %Y-%m-%d в %H:%M:%S!  │"
    echo '│                                     │'
    echo '┕━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┙'
    echo
done
